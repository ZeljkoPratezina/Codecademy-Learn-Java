//Jedna vazna karakteristika Jave je spremanje vrijednosti u varijable
//Svakoj varijabli mora biti odreden tip podataka
package lesson1_Introduction_to_Java;

public class Variables {

	public static void main(String[]args){
		//MyNumber je varijabla tipa integer i ima vrijednost 42
		int MyNumber = 42;
		//isFun je varijable tipa boolean i ima vrijednost true
		boolean isFun = true;
		//movieRating je varijabla tipa char i ima vrijednost B
		char movieRating = 'B';
		
		System.out.println(MyNumber);
		System.out.println(isFun);
		System.out.println(movieRating);
	}
	
}
