/*Relacijski operatori usporeduju tipove podataka,
 Uvijek vracaju boolean vrijednost - true ili false,
 Imamo: <, <=, >, >= */
package lesson1_Introduction_to_Java;

public class RelationalOperators {
	
	public static void main(String[]args){
		
		System.out.println(15 > 20);
		System.out.println(45 < 86 );
		System.out.println(34 >= 54);
		System.out.println(67 >= 67);
	}

}
